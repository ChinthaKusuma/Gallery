class Gallery extends Component {
  state = {
    image: imagesList[0].id,
  }

  onChange2 = id => {
    this.setState({
      image: id,
    })
  }

  render() {
    const {image} = this.state
    const list = imagesList.filter(each => each.id === image)
    return (
      <div className="bgContainer">
        <div className="bg">
          {list.map(each => (
            <img
              src={each.imageUrl}
              key={each.id}
              alt={each.imageAltText}
              className="img1"
            />
          ))}

          <h1 className="heading">Nature photography</h1>
          <p className="para">Nature Photography by Rahul</p>
          <ul className="one">
            {imagesList.map(eachItem => (
              <ThumbnaliItem
                item={eachItem}
                onChange2={this.onChange2}
                isActive={eachItem.id === image}
                key={eachItem.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
